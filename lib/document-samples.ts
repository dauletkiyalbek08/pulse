/** Уникальные ключи плейсхолдеров {{...}} в порядке появления. */
export function extractPlaceholders(body: string): string[] {
  const out: string[] = [];
  const seen = new Set<string>();
  const re = /\{\{\s*([a-zA-Zа-яёА-ЯЁ0-9_]+)\s*\}\}/g;
  let m: RegExpExecArray | null;
  while ((m = re.exec(body))) {
    const key = m[1];
    if (!seen.has(key)) {
      seen.add(key);
      out.push(key);
    }
  }
  return out;
}

/** Подставить значения; незаполненные плейсхолдеры превращаются в «_____». */
export function fillTemplate(body: string, values: Record<string, string>): string {
  return body.replace(/\{\{\s*([a-zA-Zа-яёА-ЯЁ0-9_]+)\s*\}\}/g, (_, key: string) => {
    const v = values[key];
    return v && v.trim() ? v.trim() : "__________";
  });
}

/** «дата_начала» → «Дата начала». */
export function prettifyPlaceholder(key: string): string {
  const s = key.replace(/_/g, " ").trim();
  return s.charAt(0).toUpperCase() + s.slice(1);
}

